vector<int> prefix_function(string s) {
    int n = (int)s.length();
    vector<int> pi(n);
    for (int i = 1; i < n; i++) {
        int j = pi[i-1];
        while (j > 0 && s[i] != s[j])
            j = pi[j-1];
        if (s[i] == s[j])
            j++;
        pi[i] = j;
    }
    return pi;
}

int countSubstring(string s,string sub)
{
    string v = sub + "#" + s;

    vector<int>pre = prefix_function(v);

    int subsz = sub.size();

    int cnt = 0;

    for(int i=subsz+1;i<pre.size();i++) /// starting after the sub
        if(pre[i]==subsz)
            cnt++;

    return cnt;
}

